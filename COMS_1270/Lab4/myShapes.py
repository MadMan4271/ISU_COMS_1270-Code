import math

def areaOfCircle(rad):
    return pow(rad,2) * math.pi

def areaOfRectangle(base, height):
    return base * height

def circleCircumference(rad):
    return 2 * rad * math.pi

def rectanglePerimeter(width, height):
    return 2 * width + 2* height
